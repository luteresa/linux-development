连续两周上涨，周K线都是中等长度的阳线，后市往往极度看好。在很
多次的交易中，我都利用过这条经验，基本上都有不错的收获。

本章的最后，我想再一次借用投机大师里费默的两句话作为结束：
只有既能驾驭股市又能坐得住的人才算了不起，这的确最难做
到，但只有真正做到了，投机者才能赚大钱。

“市场很明显地按我的思路进行着，我没有必要整天坐在显示牌
前，时刻盼望着得到什么信息脱手。在退却的响亮号角吹起以前，当
然，这不包括出人意料的大灾难，股市会犹豫，或者为我准备一个逆向
的投机形势。”




投机者的修养和境界并不神秘，也不玄虚，它是建立在两个方面之
上的。一是投机者对市场的理解、对市场交易中一些重大的原则性问题
的基本态度；二是投机者对自我的认识，这种认识既包括对人类千百年
来亘古不变的本性，如希望、贪婪、恐惧、侥幸、自负等的洞察和了
解，也需要投机者对自己特殊的心理弱点、性格缺陷、思维、行为习惯
等主体身心活动的深刻反省。
股票、期货市场的投机游戏是人与人之间思想、意志、心理的对抗
与较量。对市场的理解和对自我的认识这两方面之间，本质上是相互影
响、相互制约的。
早在100多年以前，一位投机市场的老手就说过这样的话：成功的
股票、期货投机定律是建立在这一假设的基础之上的，那就是，人们将
重复过去的错误。
因此，投机者对人性的认识越深刻，对市场的理解就越深入；反之
也是。从这种意义上说，股票、期货的交易实质也就是一种心理游戏，
既是与市场中其他人的角逐，更是与内心自我本能、人性弱点的对抗。

一个成功的投机高手，往往既有深刻的市场
见解，也能洞悉自己的内心世界，同时在这两方面达到很高的修为。

17境界
1.关于冲动性交易的问题
许多经验丰富的老手都认识到，冲动性交易具有巨大的破坏性，严
重威胁投机者的资金安全。
冲动性交易的主要原因是投机者缺乏足够的自我控制能力，沉溺于
短时间从市场波动中赚一笔大钱的想法而无法自拔。既不能抵挡外在的
市场波动的诱惑，也不能遏制内在强烈的交易欲望。从交易理念来说，
冲动性交易是一种短线交易，而且是超级短线。
现在，我已经很少从事短线交易，尤其是日内短线交易，知道这是
毁灭投机者最快捷的途径。我把自己的注意力完全集中在寻找、发掘中
长线交易的机会上，对短时间从市场波动中赚一笔大钱的想法已不感兴
趣。原来像裸体美女一样诱人的市场短期波动，再也不能激起我的交易
欲望和冲动。
星星还是那颗星星，月亮还是那个月亮。市场依然在激烈动荡，用
看似巨大、可观的利润，诱惑、吸引着一批又一批新老投机者前赴后
继，飞蛾扑火。而这一切，在我眼里已有了全然不同的意义。
冲动性交易，那还是一个问题吗？我问自己。

2.关于止损问题
每一本关于期货交易的书，都会不厌其烦地提醒投机者，交易不利
时要及时止损，迅速退出市场以减少损失。“如果你不愿意亏小钱，迟
早就会亏大钱”、“刚开始时的损失总是最廉价的损失”等等。
尽管有如此多的提醒和忠告，面对交易不利的局面，投机者普遍性
的习惯仍然是不愿迅速认赔，避免重大亏损的发生。理性上，一个投机
者可能认识到止损的重要性，但在实际交易中却往往做不到。

在交易明显出现不利局面时，迅速改变自己的想法、推翻原来的市
场判断、承担实实在在的损失，这一切都会给投机者带来沉重的心理、
精神负担。
问题又回到了起点。
只要我还在期货市场从事投机交易，止损是帮助我躲过重大灾难的
唯一办法。尽管每一次砍仓都要经历一次精神上痛苦的折磨，但是，鸵
鸟式的逃避显然无济于事。如果你不能控制风险，风险迟早就会控制
你。

。只要你从市场交易中赚取的利润远远大于止损时造成的
损失，你的某一笔具体交易是赚是赔无关紧要。赔小钱，赚大钱，这是
期货交易必须坚持的最简单的思考原则

