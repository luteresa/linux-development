一个人年轻时候桀骜不驯，到了不惑之年逐渐变得循规蹈矩，可能
是大多数人的必由之路，也可能是人生的某种必然吧。

人的命运可以自己选择吗？可以，你可以走你愿意走的路。但是，
一旦你上了路，回头恐怕就没有那么容易了，人生的许多事情无法重来
一次。

作为一个投机者，为了在市场波动中赚取利润，每天早上到下午，
我一直盯着国内期货市场的价格变化，忙忙碌碌地买进卖出。晚上，因
为国内国际市场联动，我需要关注芝加哥、纽约、伦敦市场各个品种的
走势，也不敢放心大胆地睡觉。偶尔，当外盘暴涨暴跌，市场走势对我
极为有利时，真想把已经酣然进入梦乡的朋友们吵醒，与他们分享快
乐；当外盘走势出现不测，和我的预期背道而驰的时候，却只能独自苦
笑，无奈地痛骂自己一顿：为何做了这么多年期货还像个白痴，竟然会
持有这么愚蠢的头寸！哎，看来明天开盘又得手忙脚乱地忙一阵子，实
在不行就狼狈逃命吧！

因此，在相当长一段时间，我在股票交易中一直遵循着下面三条基
本原则：
第一，用政治眼光来审视股票市场，抓住市场可能出现的重大变
化。国内股票市场不但过去是政策市，我觉得，今后相当长时间之内，
它依然不能完全摆脱政策市的阴影。这一点意味着，股票市场一旦出现
过于离谱的暴涨暴跌，就随时有可能出现一种外在的力量来左右市场走
势，使股市回到社会政治所能容忍的范围以内。一定的政治敏感度，是
国内股市投机者抓住战略性投资机会、获得暴利的指南针。
第二，抓住大行情可能来临的机会，长线交易、重仓交易，不贪
图小利，迷恋股市，整天在市场中炒来炒去。
第三，绝不染指垃圾股，因为垃圾股即使在大牛市中也可能没有
出头之日。即使手中股票暂时被套住，也有翻身的日子。适当的分散投
资可能是避免受到垃圾股伤害的一个简单策略。

十多年以后的今天，如果重新审视这个问题，我的认识是：预测制
胜是一条不可能成功的交易歧路，世上没有一个人、一种工具可以准
确地预测市场的未来变化。市场交易是一个动态、系统的控制过程。
理解市场价格变化、判断未来走势只是成功交易的一个环节，而且应该
不是最重要的环节。在实际操作中，每一次交易投入的资金多少；情况
不利时如何处理、有利时如何加码；加码后出现有利或者不利情况如何
应对；赢利的交易怎样既不由赢转亏，又不丧失一旦市场出现大波动带
来的暴利等等，应对这些问题的技巧、策略和方法，远比仅仅希望准确
的预测市场走势重要得多，需要投机者有一套完整、系统的交易思路。

