。首先，市场价格，尤其是
短期价格变化能不能准确预测，实在是一个非常令人生疑的问题。我的
经验是，对一个投机者来说，比较明智的态度是，相信市场价格不能被
准确预测，把不确定性作为市场的本质，在此基础上再构建自己的交易
策略。其次，价格预测只是每一次交易的起点，只是万里长征的第一
步。从一个专业投机者的角度看，任何一笔具体的交易，都需要考虑后
面一系列更为重要的问题：第一次入市的仓位大小，止损点的确定，赢
利后什么点位加码，加多少量，多少次，加码后情况不妙怎么办，市场
出现意外情况如何处理，赢利目标在哪里，等等。显然，价格预测并不
是交易中最关键的部分。

就某一两次
局部的交易而言，确实，你对市场价格走势判断是否准确，直接决定了
交易结果是赢还是亏。但是，在成千上万次的交易以后，总的投机结
果，并不取决于局部一两次猜测的准确与否，而是取决于建立在投机者
综合实力和水平之上的市场、心理优势。投机者的市场认识、交易理
念、策略技巧、心理素质、修养境界等因素，比起市场预测能力来，对
最后的成败所起的作用更大。

一旦你在市场中建立了头寸，你对市场走势的判断就
不像原来作为旁观者时那么客观、超然、自信，你的心理、情绪会发生
巨大的变化，怀疑、焦虑、恐惧、患得患失等主观因素很可能扭曲你的
思维，使你在实际操作时陷入混乱——反复无常、自相矛盾，等等。结
果，看起来一次很单纯的顺势交易，在人的心理因素的干扰下，你也会
把它搞得乱七八糟。事后看来，你当初对市场走势的判断完全准确，但
是，很可能你非但没有从自己聪明的判断中赚到钱，还有可能亏钱。


现在我完全根据图表的信号交易，情况就大不一样了。当市场价格
有较大的波动时，我会随时用铅笔在图表上画出来，研究、分析市场目
前的状态和下一步的可能变化。
如果我手中的图表显示，市场中长期趋势上涨，而当天的盘面价格
却在下跌，只要下跌的幅度在中期趋势可以容忍的范围之内，我就不会
入市放空。相反，我可能会逆着市场的盘面走势，逢低做多。
如果图表显示市场中长期趋势是下跌，而当天的盘面价格却在反
弹，只要反弹的幅度在中期趋势可以容忍的范围之内，我就不会入市做
多。相反，我可能会逆着市场的盘面走势，逢高放空。
依靠图表显示的市场趋势操作，我的思维不再每天在多空之间摇摆
不定，变来变去。在市场短期波动的诱惑和干扰面前，我的情绪在理性
地审视后，得到了有力的控制，避免了一时冲动做出的不理智决策。
以前，我经常在盘中追涨杀跌，极短的时间内就从空头转向多头，
又从多头转向空头，反复无常。凭感觉短线交易的策略使我控制不住内
心的贪婪和恐惧，抵挡不住市场的诱惑。
现在，由于主要着眼于市场中期趋势，我的交易心理和行为变得相
对稳定、靠谱。面对每天的盘面变化，我不再神经兮兮，精神紧张，操
作次数也大为减少。第一周交易结束，我不但把第一天损失的16万元全
部弥补回来，而且还有20％多的赢利。第二、第三周，我又连续在市场
中获得了20％多的利润。

当市场价格跌到比前期低点高20来点的位置，我试探性地入市做
多。我的想法是，如果我对市场的中期走势判断错误，市场价格跌破前
一段时间形成的历史低点，就认赔出局。如果我的判断正确，我买入的
位置应该是一个很好的点位。一旦第一次交易有了浮动赢利，我将逐步
加码。


一条非
常简单而又实用的交易规则，使我避免了两次意外的伤害。这条交易规
则是：在期货交易中，如果投机者入市后三天还没有出现利润，立刻
平仓出场。我也记不清是在哪一本书上看到这个规则的，反正，我一到
商品期货市场，就非常严格而且虔诚地遵守着这一原则。

1．进入商品期货交易的第一个月，我的精神、心理状态非常好。
作为一个市场新手，因为对期货不熟悉，害怕损失，我对期货交易
抱着非常谨慎的态度。战战兢兢，如履薄冰，如临深渊。所以，每一次
入市交易前，我都会反复思考，再三权衡，在交易机会选择时非常慎
重。
作为一个市场新手，没有急躁、贪婪的欲望，也没有奢望过一夜之
间赚大钱。这样，我就能够理智、平静地看待市场波动，不慌不忙地选
择出入市机会。淡泊的心态使我的思维单纯、清晰，容易和市场运动的
节奏合拍，没有大的冲突。
作为一个市场新手，我还没有交易上的损失，所以，没有精神压力
和历史包袱，交易起来比较自由、放松。许多老手业绩不佳的一个重要
原因，就是他们在市场中吃过的亏太多，经历过痛苦的事太多，已经变
成了惊弓之鸟。即使很好的机会来临，也可能缩手缩脚，在应该大胆行
动时犹豫不决。
2．中线顺势操作策略
在国债期货交易中，因为一次偶然的原因赚了一笔大钱，所以，我
最后的交易结果还不错。但是，如果把这一次特殊情况下的赢利除外，
在大多数时间，我的国债期货交易是亏钱的。我以为，这就是频繁短线
操作的结果。我早就对自己的交易方法产生了怀疑，但苦于没有找到其
他合适的投机策略。
国债期货停止交易以后，我认真反省了自己的交易方法，也从别人
身上学到了一些经验。我认识到，中线顺势交易是期货赚大钱的最佳策
略。所以，一进入商品期货市场，我就非常自觉地运用上这种策略。
在我第一个月的商品期货交易利润中，有70％来自于绿豆9509的中
线投机。从短线炒作转到中线交易，是我这一个月期货交易成功的关
键。
3．严格的风险控制
在我第一天的交易中，第一笔绿豆交易就损失了本金的8％，但在
尾市，我能果断地砍仓离场，对风险采取严格的控制态度。后来在橡胶
以及另外一次绿豆交易中，尽管我的单子并没有亏损，根据三天交易规
则，我还是及时平仓离场，避免了意外的损失。
如果我没有采取严格的风险控制措施，这三次的交易损失可能会夺
走我大量的利润，那样这个月的业绩也就很一般了。
4．加码策略
在绿豆交易中，我很自觉地运用了试探——加码的交易策略。即在
第一次投资以后，如果市场走势证明我的判断错误，我就止损离场。如
果随后的市场走势表明我的判断正确，我就会在第一笔交易有了浮动赢
利以后，再一次一次地加码。这样，我在失误时，只会损失一点小钱，
因为刚开始时我投入的资金量不大。而一旦我的市场判断正确，通过加
码策略，我就会赚到大钱。试探——加码的交易策略让我赚大钱，亏小
钱。
5．一点运气
在我进入商品期货市场时，国内各个商品交易所中，很多品种都非
常活跃，市场机会多、价格波动幅度大，这也是我能取得成功的重要原
因。如果是我在1998年到2002年这一段时间进入期货市场，由于市场交
易品种少，价格波动幅度小，绝对不可能取得如此高的投资回报。

在期货交易中，一个人的心理、精神状态远比他拥有的市场知识和
预测技术重要得多。


有人说，期货交易的技术和知识，学一两年时间可能就够了，但
是，投机者要培养出一个稳定、良好的心态，在修养和境界上达到高瞻
远瞩、超凡脱俗，即使花上十年工夫，也不算多。此言善矣！

