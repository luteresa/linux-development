买进做多；反之，我就卖出做空。
我进场以后，如果市场随后的走势没有证明我是正确的，我一般会
在出现10到20点左右的亏损时就砍仓离场。而一旦方向准确，我就持仓
等待。在很多次交易中，我都获得了200点以上的利润。这样，只要我
做对一次交易，即使随后犯了十次错误，也不会有大的损失。

有了这套交易系统，在随后一段时间的天津红小豆交易中，我不再
神经紧张地盯着盘面变化，整天揣摩主力的动向，希望预测市场未来的
走势。现在，我唯一需要关注的就是市场价格什么时候和移动平均线交
叉，只要市场一出现我设定的行动信号，我就迅速出击。如果没有交易
信号出现，我就袖手旁观，在场外耐心等待。

后来，在上海橡胶市场的实际交易中，我发现这种做法有问题。有
些突破信号发生以后，我入市交易的利润可能很大；有些信号则只有一
点蝇头小利，而且如果我平仓速度慢一点的话，非但这点小小的浮动赢
利会转瞬即逝，而且还经常出现大的损失。
所以，我就加了另外的一个规则来作为我的交易系统的补充，限制
某些方向的交易。即遵守这样一个原则：多头市场不放空，空头市场不
做多。
这样，我的交易就变成了顺势而为，交易次数确实减少了一些，但
安全性、可靠性增加了。

在空头市场中，根据我的交易规则，每次市场反弹结束，跌破55天均线时就大胆放空。
这是一种有约束、有优势的交易模式。
在多头市场中，市场价格跌破55天均线，不做空。而当市场调整结
束，市场价格重新向上穿越55天均线时，入市做多。止损点放在当日最
低位以下。
在空头市场中，市场价格向上穿过55天均线，不做多。而当市场反
弹结束，市场价格重新向下穿越55天均线时，入市做空。止损点放在当
日最高点位以上。

现在，包括55天均线在内的不同参数的移动平均线，已经成为我观
察、理解市场，把握重大交易机会的核心工具。只有当市场发出明确的
突破信号，符合我的交易模型时，我才入市交易。
我认为，投机者应该走在市场的前面，不能只是让市场牵着自己的
鼻子走。如果投机者头脑中没有像移动平均线这样一些事先预定的原
则、框架、模型，那么，漫无目的的市场观察，得到的将只是随机的、
没有规律的价格波动。
在我看来，移动平均线不是一种市场价格预测工具，而是帮助投机
者监控市场、提高交易胜算的操作工具。而这也是我在天津红小豆这个
妖异至极的市场气氛下体悟最深的一环。

