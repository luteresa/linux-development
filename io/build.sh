羽黑法

我自己的亲身经历加上10多年来在期货市场中的所见所闻表明，因
为重仓交易而爆仓的投机者，数量上要远远少于因为逆势操作而破产的
投机者。

一旦发现自己的操作和市场趋势相反，投机者绝对不能
有侥幸心理，应该在第一时间立刻平仓。这是期货交易的一条基本原则
——止损原则。高明的投机者在顺势交易时，能放胆去赢，在判断错误
时，又能果断地壮士断臂。

这次交易让我认识到，在期货投机中，可做可不做的交易，还是
不要去碰为妙。投机者如果把精力集中在很有把握、非做不可的交易上
面，交易的业绩可能会大幅提升。

期货交易有时候确实令人奇怪，大多数人往往对市场可能来临的重
大波动反应迟钝，视而不见。无论某个商品的价格上涨或者下跌到多么
不合情理的地步，事后看来这个位置是多么的荒唐和离谱，身处其中的
投机者却往往当局者迷，很难摆脱当时的市场环境、气氛的巨大影响。
面对一种历史性的市场价格，投机者很难觉察到这有什么特别之处，认
为一切依然很正常、合理。

事后从图表上看，海南橡胶市场的这波行情，下跌趋势非常明显，
逆势交易者简直是个大笨蛋。在这样单纯的走势中，我非但没有赚到
钱，反而亏了大钱，确实令局外人难以理解。作为一个专业投机者，有
好几次，当我向别人解释这段时间的交易我是如何赔钱的时候，简直羞
于启口，内心感到非常惭愧。那么，我当时做多海南橡胶的理由是什么
呢？
1．短线交易方式隐含的天然缺陷
市场趋势的力量确实非常强大，我做梦都没有想到，第二天，橡胶
市场竟然会低开200多点，前一天尾市的强势消失得无影无踪
我想利用短线操作捕捉更多的市场机会，看似聪明，实则愚笨；看
似跑得很快，实则离目标越来越远。
有一句名言说得好：过于注重细节的人，往往对大事情一无所知。
2．总比市场慢一拍的思维
投资大师巴菲特曾经挖苦过那些基金经理们，说他们总是喜欢看着
汽车的后视镜开车。巴菲特的意思是说，基金经理们总是犯经验主义错
误，以为历史会简单地重复，未来就是过去的再现。

3．我迷失在短期波动的大海里
投机者很容易被市场短期波动搞糊涂，沉溺其中而失去超然的思
维、心态，忘记市场大趋势的方向。我在橡胶市场陷入逆势交易的境
地，从某种意义上说，并不是我故意要和市场作对，而是因为短线操作
的习惯，让我不知不觉中，稀里糊涂地变成了一个逆势交易者。

4．市场趋势会突然反转吗？——新手的心理、思维陷阱
记得有一个投机高手曾经表达过这样一个观点：在市场交易中，投
机者应该做出正确的决策，而不是做出让自己感觉舒服的决策。心理上
很舒服的交易，结果往往不好。
5．R708市场价格的下跌过程中，持仓量每天都在增加
从中，我学到了两点：
一是在市场价格判断上，价格变化本身透露出的信息远比持仓量、
成交量的意义重要。在早期的期货交易中，我曾经对此迷惑不解。为了
预测市场价格的走势，把很多精力集中在分析持仓量、成交量的微妙变
化上，钻牛角尖，误入歧途。
二是对一个投机者来说，不要去参考市场中别人的行为，从众是通
向地狱最廉价的门票。

从理智上说，当市场价格
跌破我的入市点时，我必须平仓出局，控制风险；但从感情上，我又舍
不得在亏损的情况下离场。
现代决策心理学的研究表明，当一个人的内心充满矛盾和冲突时，
最典型的行为特征是不采取任何行动。

？由此，我们可以学到两点有价值的教
训：①没有有效止损的投机交易极为危险，亏得倾家荡产是早晚的事；
